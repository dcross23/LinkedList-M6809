.module leer_decimal

;================================================================================|
;				CONSTANTES					 |  <------------------------	
;================================================================================|
teclado  .equ 0xFF02								 ;
;================================================================================|




;================================================================================|    
;				VARIABLES					 |  <------------------------	
;================================================================================|    
num: .word 0								  	 ;						
;================================================================================|




;================================================================================|
;			   FUNCIONES GLOBALES					 |  <------------------------	
;================================================================================|												
	.globl leer_decimal
;================================================================================|



;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
; leer_decimal                                                     ;
;     lee por teclado un numero decimal de tres cifras       	   ;
;     y lo almacena en D					   ;
;                                                                  ;
;   Entrada: ninguna				                   ;
;   Salida:  D -> numero decimal de tres cifras                    ;
;   Registros afectados: D,CC.                                     ;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

leer_decimal:
;---------------------- LEER DECIMAL ------------------------------;
	;pongo la variable a 0					   ;
	ldd #0							   ;
	std num							   ;
								   ;
	;1er numero						   ;
	lda teclado						   ;
	suba #'0						   
	ldb #100						   ;
	mul 						   	   ;
	addd num						   ;
	std num						      	   ;
								   ;
	;2o numero						   ;
	lda teclado 						   ;
	suba #'0
	ldb #10						   	   ;
	mul							   ;
	addd num						   ;
	std num						   	   ;
	clra							   ;
								   ;
	;3er numero						   ;
	ldb teclado      					   ;
	subb #'0
	addd num 						   ;
;------------------------------------------------------------------;
	rts




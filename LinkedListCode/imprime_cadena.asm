	.module imprimecadena


;================================================================================|
;				CONSTANTES					 |  <------------------------	
;================================================================================|
pantalla    .equ 0xFF00			;salida por pantalla			 ;
;================================================================================|



;================================================================================|    
;				VARIABLES					 |  <------------------------	
;================================================================================| 	
;================================================================================|



;================================================================================|
;			   FUNCIONES GLOBALES					 |  <------------------------	
;================================================================================|
	.globl imprime_cadena
;================================================================================|


;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
; imprime_cadena                                                   ;
;     saca por la pantalla la cadena acabada en '\0 apuntada por X ;
;                                                                  ;
;   Entrada: X -> direccion de comienzo de la cadena               ;
;   Salida:  ninguna                                               ;
;   Registros afectados: X, CC.                                    ;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
 
imprime_cadena:
;-------------------- IMPRIMIR CADENA -----------------------------;
sgte:   lda ,x+							   ;
        beq ret_imprime_cadena					   ;
        sta pantalla						   ;
        bra sgte						   ;
;------------------------------------------------------------------;
ret_imprime_cadena:
        rts
